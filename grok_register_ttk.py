def mail_create_temp_address():
    """创建临时邮箱：POST /api/v1/addresses → 返回 (email, token)。

    接口无鉴权；username 仅允许字母数字下划线。使用 hex 保证字符集合法。
    """
    api_base = get_mail_api_url() or MAIL_API_URL_DEFAULT
    domain = get_mail_domain() or MAIL_DOMAIN_DEFAULT
    username = "u_" + secrets.token_hex(6)
    url = f"{api_base}/api/v1/addresses"
    resp = http_post(
        url,
        json={"username": username, "domain": domain},
        headers={"Content-Type": "application/json"},
        verify=False,
    )
    resp.raise_for_status()
    try:
        data = resp.json()
    except Exception:
        raise Exception(f"创建邮箱返回非JSON: {resp.text[:300]}")
    email = data.get("email")
    token = data.get("token")
    if not email or not token:
        raise Exception(f"创建邮箱响应缺少 email/token: {data}")
    return email, token


def mail_get_inbox(token):
    """读取收件箱：GET /api/v1/{token}/emails → 返回邮件列表。"""
    api_base = get_mail_api_url() or MAIL_API_URL_DEFAULT
    resp = http_get(f"{api_base}/api/v1/{token}/emails", verify=False)
    resp.raise_for_status()
    try:
        data = resp.json()
    except Exception:
        raise Exception(f"读取收件箱返回非JSON: {resp.text[:300]}")
    if isinstance(data, list):
        return data
    if isinstance(data, dict):
        emails = data.get("emails")
        if isinstance(emails, list):
            return emails
    return []


